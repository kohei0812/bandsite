<section id="section-2">
 <h2>HISTORY</h2>
 <p>～2017年 約20年ほど四宮立、川端康平は各々バンド活動をしていた。四宮立のメンバー募集は非喫煙者が絶対条件なのでメンバー探しに難航し、主にソロで活動していた。ようやく集めたメンバーでPreppyというバンドを結成するが、ほどなく解散。</p>
 <br>
 <p>2017年9月 ソロで活動していた四宮立(Dr)と別バンドで活動していた川端康平(Gt)が、川端康平の企画したイベントで対バンし、楽屋で意気投合、次の日にバンド結成をする。</p>
 <br>
 <p>2018年1月 Stacked Stateとして初ライブをする。</p>
 <br>
 <p>2018年 春頃 Preppyのギタリスト岡本振一郎が自身の強い希望によりStackedStateに合流。ここでようやく３人体制となる。</p>
 <br>
 <p>2019年3月 ひょうんなつながりから、東南アジア（タイ、マレーシア、シンガポール）ツアーを敢行することに。初日の飛行機に乗り遅れてタイでのライブに遅刻しかける。タイのライブの次の日のタイオーガナイザーの言葉「Last night was crazy.」</p>
 <br>
 <p>2020年夏 2019年、2020年と各メンバーのプライベートの事情やコロナの影響で活動休止することが多かったが、本格的に活動再開をはじめる。</p>
